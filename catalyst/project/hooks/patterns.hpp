static constexpr size_t kPatternCount = sizeof(g_HookPatterns) / sizeof(g_HookPatterns[0]);

inline const HookPattern* FindPatternByHookID(uint8_t hook_id) {
    for (size_t i = 0; i < kPatternCount; ++i) {
        if (g_HookPatterns[i].hook_id == hook_id)
            return &g_HookPatterns[i];
    }
    return nullptr;
}

inline const HookPattern* FindPatternByName(const char* name) {
    for (size_t i = 0; i < kPatternCount; ++i) {
        if (strcmp(g_HookPatterns[i].name, name) == 0)
            return &g_HookPatterns[i];
    }
    return nullptr;
}

inline const HookPattern* GetPattern(uint8_t hook_id) {
    return FindPatternByHookID(hook_id);
}

inline size_t GetPrologueSize(uint8_t hook_id) {
    auto* p = FindPatternByHookID(hook_id);
    return p ? p->prologue_size : 16;
}

